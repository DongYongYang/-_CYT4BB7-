#include "zf_common_headfile.h"
// 打开新的工程或者工程移动了位置务必执行以下操作
// 第一步 关闭上面所有打开的文件
// 第二步 project->clean  等待下方进度条走完

#define FLASH_SECTION_INDEX       (0)                                           // 存储 M0数据地址及长度 用的扇区
#define FLASH_PAGE_INDEX          (0)                                           // 存储 M0数据地址及长度 用的页码

 
#define DATA_LENGTH               (5)                                           // 数组数据长度

uint32 m0_data[DATA_LENGTH] = {0, 1, 2, 3, 4};                                  // 定义M0演示数据数组 整数类型

int main(void)
{
    __enable_irq();
    SystemInit();                                                               // 系统初始化 对系统时钟、系统电源进行配置
    debug_init();			                                        // 初始化默认调试串口

    // 此处编写用户初始化代码 
    
    flash_init();                                                               // flash初始化(访问flash之前需要初始化一次)
    
    flash_union_buffer[0].uint32_type = (uint32)&m0_data[0];                    // 取数组首地址
    flash_union_buffer[1].uint32_type = DATA_LENGTH;                            // 取数组长度
        
    flash_erase_page(FLASH_SECTION_INDEX, FLASH_PAGE_INDEX);                    // 写入flash之前需要擦除当前页 否则可能导致数据错误
    flash_write_page_from_buffer(FLASH_SECTION_INDEX, FLASH_PAGE_INDEX, 2);     // 将地址和长度信息写入flash
    
    // 此处编写用户初始化代码 
    
    Cy_SysEnableApplCore(CORE_CM7_1, CY_CORTEX_M7_1_APPL_ADDR);                 // 启动M7核心1
    Cy_SysEnableApplCore(CORE_CM7_0, CY_CORTEX_M7_0_APPL_ADDR);                 // 启动M7核心0
    while(true)
    {
        // 此处编写需要循环执行的代码
        
        system_delay_ms(1000);                                                  // 主循环延时
        
        for(int i = 0; i < DATA_LENGTH; i ++)                                   // M0演示数据自增 M0核心没有cache 所以无需cache相关操作
        {
            m0_data[i] ++;
        }

        // 此处编写需要循环执行的代码
    }
}

